static void conf(struct menu *menu)
{
 struct symbol *sym;
 struct property *prop;
 struct menu *child;

 if (!menu_is_visible(menu))
  return;

 sym = menu->sym;
 prop = menu->prompt;
 if (prop) {
  const char *prompt;

  switch (prop->type) {
  case P_MENU:




   if (input_mode != oldaskconfig && rootEntry != menu) {
    check_conf(menu);
    return;
   }

  case P_COMMENT:
   prompt = menu_get_prompt(menu);
   if (prompt)
    printf("%*c\n%*c %s\n%*c\n",
     indent, '*',
     indent, '*', prompt,
     indent, '*');
  default:
   ;
  }
 }

 if (!sym)
  goto conf_childs;

 if (sym_is_choice(sym)) {
  conf_choice(menu);
  if (sym->curr.tri != mod)
   return;
  goto conf_childs;
 }

 switch (sym->type) {
 case S_INT:
 case S_HEX:
 case S_STRING:
  conf_string(menu);
  break;
 default:
  conf_sym(menu);
  break;
 }

conf_childs:
 if (sym)
  indent += 2;
 for (child = menu->list; child; child = child->next)
  conf(child);
 if (sym)
  indent -= 2;
}

static void check_conf(struct menu *menu)
{
 struct symbol *sym;
 struct menu *child;

 if (!menu_is_visible(menu))
  return;

 sym = menu->sym;
 if (sym && !sym_has_value(sym)) {
  if (sym_is_changeable(sym) ||
      (sym_is_choice(sym) && sym_get_tristate_value(sym) == yes)) {
   if (input_mode == listnewconfig) {
    if (sym->name) {
     const char *str;

     if (sym->type == S_STRING) {
      str = sym_get_string_value(sym);
      str = sym_escape_string_value(str);
      printf("%s%s=%s\n", CONFIG_prefix(), sym->name, str);
      free((void *)str);
     } else {
      str = sym_get_string_value(sym);
      printf("%s%s=%s\n", CONFIG_prefix(), sym->name, str);
     }
    }
   } else if (input_mode == helpnewconfig) {
    printf("-----\n");
    print_help(menu);
    printf("-----\n");

   } else {
    if (!conf_cnt++)
     printf("*\n* Restart config...\n*\n");
    rootEntry = menu_get_parent_menu(menu);
    conf(rootEntry);
   }
  }
 }

 for (child = menu->list; child; child = child->next)
  check_conf(child);
}

